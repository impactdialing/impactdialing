class ApplicationPolicy
  attr_reader :user, :record

private
  def admin?
    user.administrator?
  end

  def current_user?
    user.administrator? || user.supervisor?
  end

public

  def initialize(user, record)
    @user = user
    @record = record
  end

  def index?
    admin?
  end

  def show?
    admin?
  end

  def create?
    admin?
  end

  def new?
    create?
  end

  def update?
    admin?
  end

  def edit?
    update?
  end

  def destroy?
    admin?
  end

  def scope
    Pundit.policy_scope!(user, record.class)
  end

  class Scope
    attr_reader :user, :scope

    def initialize(user, scope)
      @user = user
      @scope = scope
    end

    def resolve
      scope.where(account_id: user.account_id)
    end
  end
end
